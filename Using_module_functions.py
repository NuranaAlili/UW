# Import the seed() function from the random module.
# This function sets the "seed" value used to generate random numbers.
# Random numbers are generated by taking a seed value and then performing mathematical transformations to it to produce the "random" number.
# The resulting value is then used as the "seed" for the next random number generated, allowing sequences of "pseudorandom" numbers to be produced. See here for details.
# The seed value is normally taken from "noisy" values (such as based on the computer's clock), but the seed() function allows you to effectively specify "which sequence" of random numbers will be produced.


from random import seed

