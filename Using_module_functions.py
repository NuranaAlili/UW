# Import the math module so you can use some mathematical functions.

import math

# Choose any number between 0 and 360 and assign it to a variable angle_degrees.


angle_degrees = 45

# Use an appropriate function from the math module to convert the degrees value into radians.
# Store the result in a variable theta (also written as θ,
#     but you can't name a variable that!). Print out the value of theta.


theta = math.radians(angle_degrees)
print(theta)


# Use a function from the math module to calculate the tangent of theta, and print out the result.
# You do not need to make a separate variable.


print(math.tan(theta))

# Calculate tan(θ+π). You can get a value of pi as a variable from the math module.
# Print your result, and visually confirm that it's the same as
# tanθ that you calculated above (though there may be some "precision" errors)


print(math.tan(theta+math.pi))

